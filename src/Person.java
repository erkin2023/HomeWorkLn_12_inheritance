public class Person {
    String learn ;
    String walk;
    String eat;

    public Person(String learn, String walk, String eat) {
        this.learn = learn;
        this.walk = walk;
        this.eat = eat;
    }

    public String learn(){
        return learn;
    }
    public String walk (){
        return  walk;
    }
    public String eat (){
        return eat;
    }
    public Person() {
    }


}
